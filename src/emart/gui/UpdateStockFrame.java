    private void LoadProduct() {
              try{
       List<ProductPojo> sp = ProductDao.getProductDetails();
       if(sp.isEmpty()){
           JOptionPane.showMessageDialog(null, "No Product Present in Stock");
           return;
       }
               btnUpdate.setEnabled(true);
             dm = (DefaultTableModel)jProduct.getModel();
            Object [] ob = new Object[7];
            for(ProductPojo pj:sp){
                ob[0] = pj.getProductId();
                ob[1] = pj.getProductname();
                ob[2] = pj.getProductcompany();
                ob[3] = pj.getProducprice();
                ob[4] = pj.getOurprice();
                ob[5] = pj.getQuantity();
                ob[6] = pj.getTax()+"%";
                dm.addRow(ob);
            }     
        }
        catch(SQLException ex){
        JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
    }


    }
