    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if(!ValidateInput()){
            JOptionPane.showMessageDialog(null, "Please fill the field");
            return;
        }
        try{
            int n = jtax.getSelectedItem().toString().trim().length();
            int tax = Integer.parseInt(jtax.getSelectedItem().toString().substring(0, n-1).trim());
            ProductPojo pj = new ProductPojo();
            pj.setProductId(jProductId.getText().trim());
            pj.setProductId(JProductName.getText().trim());
            pj.setProductcompany(jProductCompany.getText().trim());
            pj.setProducprice(Double.parseDouble(jProductPrice.getText().trim()));
            pj.setOurprice(Double.parseDouble(jOurPrice.getText().trim()));
            pj.setQuantity(Integer.parseInt(Jquantity.getText().trim()));
            pj.setTax(tax);
            boolean result = ProductDao.updateProduct(pj);
            System.out.println("Data Update Successfully");
            if (result) {
                JOptionPane.showMessageDialog(null, "Product Update successfully");
                dm.setRowCount(0);
                LoadProduct();
                return;
               }
            JOptionPane.showMessageDialog(null, "Product Updation failed!");
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "DB Error." + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "PLease Insert Numeric Value!!" + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

