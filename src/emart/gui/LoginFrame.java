    private boolean ValidateInput() {
       userid = txtUserId.getText().trim();
       char [] c = PasswordF.getPassword();
       if(userid.isEmpty() || c.length == 4){
           JOptionPane.showMessageDialog(null, "Please input four charecter in password field");
           return false;
       }
           else {
            pass = new String(c);
            return true;
       }
    }

