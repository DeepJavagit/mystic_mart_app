    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        if(!ValidateInput()){
            JOptionPane.showMessageDialog(null, "Please input id and password");
            return;
        }
        String usert = null;
        usert = this.CheckUserType();
        if(usert == null){
            JOptionPane.showMessageDialog(null, "Please Select UserType!","Message",JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        
        
        try{
            UsersPojo user = new UsersPojo();
            user.setUserid(userid);
            user.setPassword(pass);
            user.setUsertype(usert);
            boolean userName = UserDao.ValidateUser(user);
            if(userName == false){
                 JOptionPane.showMessageDialog(null, "Invalid userid/paasword/usertype!","Error!",JOptionPane.ERROR_MESSAGE);
                 return;
            }
            UserProfile.setUserid(userid);
            UserProfile.setUsertype(usert);
            if(usert.equalsIgnoreCase("Manager")){
                ManagerOptionFrame mf = new ManagerOptionFrame();
                mf.setVisible(true);
                this.dispose();
            }
            else if(usert.equalsIgnoreCase("Receptionist")){
              ReceptionistOptionFrame rf = new ReceptionistOptionFrame();
              rf.setVisible(true);
              this.dispose();
            }
            
        } catch (SQLException ex) {
              JOptionPane.showMessageDialog(null, "No UserId/Password Found! Try Again." + ex.getMessage(), "Exception", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
         }
        
    }//GEN-LAST:event_btnloginActionPerformed

