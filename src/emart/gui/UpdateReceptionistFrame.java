    private void loadReceptionistDetails() {
    try{
        receptionist = ReceptionistDao.getAllReceptionistId();
        if(receptionist.isEmpty()){
            JOptionPane.showMessageDialog(null, "No receptionist present!!");
            return;
        }
        Set<String> keys = receptionist.keySet();
        for(String id:keys){
           jcReceptionistId.addItem(id);
        }    
    }
    catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "DB Error","Error!",JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }

    private boolean ValidateInputs() {
       char [] pwd = jPasswordField2.getPassword();
       char [] pwd2 = jPasswordField3.getPassword();
       if(pwd.length < 4 || pwd2.length < 4){
             JOptionPane.showMessageDialog(null, "Password must be four charecter in lenght");
             return false;
       }
       password = String.valueOf(pwd);
       String repassword = String.valueOf(pwd2);
       if(!password.equals(repassword)){
            JOptionPane.showMessageDialog(null, "Password not be Same");
            return false;
       }
       return true;    
    }

    private void clearText() {
       jPasswordField2.setText("");
       jPasswordField3.setText("");
    }
