    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!ValidateInputs()){
            return;
        }
        try{
            String userId = jcReceptionistId.getSelectedItem().toString();
            boolean result = ReceptionistDao.updateReceptionist(userId, password);
            if(result){
                JOptionPane.showMessageDialog(null, "Password change Succesfully");
                clearText();
                jcReceptionistId.setSelectedIndex(0);
                return;
            }
            JOptionPane.showMessageDialog(null, "Password Not change");
            clearText();
        }
        catch(SQLException ex){
        JOptionPane.showMessageDialog(null, "DB Error","Error!",JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
           
    }//GEN-LAST:event_jButton1ActionPerformed

