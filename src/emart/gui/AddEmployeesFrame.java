    private boolean ValidtateInput() {
        empname = txtName.getText().trim();
        empSal = txtSal.getText().trim();
        if(empname.isEmpty() && empSal.isEmpty()){
           return false;
        }
        return true;
    }

    private void loadEmployeeId() {
        try{
       txtEmpId.setText(EmpDao.getEmployeeId());
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "EMPID not genrate" + ex.getMessage(), "Exception", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    private void clearText() {
        txtName.setText("");
        txtSal.setText("");
    }
