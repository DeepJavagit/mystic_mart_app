    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!ValidtateInput()){
            JOptionPane.showMessageDialog(null, "Please input all The Feild");
        }
        try{
        int esal = Integer.parseInt(empSal);
        EmpPojo ep = new EmpPojo();
        ep.setId(txtEmpId.getText());
        ep.setName(empname);
        ep.setJob(jJob.getSelectedItem().toString());
        ep.setSal(esal);
        boolean result = EmpDao.AddEmp(ep);
        if(result){
          JOptionPane.showMessageDialog(null, "Record addedd successfully");
          loadEmployeeId();
          clearText();
          return;
        }
        } catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null,"Please input numeric salary!"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
       catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

